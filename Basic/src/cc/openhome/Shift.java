package cc.openhome;

public class Shift {

	public static void main(String[] args) {
		int number = 1;
		
        System.out.printf("2的0次方:%d%n",number);
        System.out.printf("2的1次方:%d%n",number<<1);
        System.out.printf("2的2次方:%d%n",number<<2);
        System.out.printf("2的3次方:%d%n",number<<3);
	}

}
